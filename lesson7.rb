#upcase（小文字を大文字に変換）
puts "webcamp" .upcase


#FYI
#upcase!（小文字を大文字に変換／破壊的メゾッド）
puts "WebCamp" .upcase!

#downcase（大文字を小文字に変換）
puts "WEBCAMP" .downcase

#downcase（大文字を小文字に変換／破壊的メゾッド）
puts "WebCamp" .downcase!

#swapcase（小文字を大文字に、大文字を小文字に）
puts "WebCamp" .swapcase

#swapcase!（小文字を大文字に、大文字を小文字に／破壊的メゾッド）
puts "WebCamp" .swapcase!

#capitalize（先頭の小文字を大文字に変換）
puts "webcamp" .capitalize

#capitalize!（先頭の小文字を大文字に変換／破壊的メゾッド）
puts "webcamp" .capitalize!



